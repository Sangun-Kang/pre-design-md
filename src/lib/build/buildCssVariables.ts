import type { DesignDecisions, ShadowInput } from '../../types/design';
import { buildTypeScale } from '../typeScale';
import { buildSpacingScale } from '../spacingScale';
import { buildRadiusScale } from '../radiusScale';
import { buildShadowTokens } from '../shadowTokens';
import { buildColorPalette } from '../colorPalette';

/**
 * Plain `.css` output. Drop-in :root custom properties — no markdown,
 * no prose, no AI instructions. OKLCH preserved for colors so users on
 * modern CSS keep the wide-gamut source.
 */
export function buildCssVariables(d: DesignDecisions): string {
  const sections: string[] = ['/* Generated by pre-design-md */', '', ':root {'];

  if (d.typography) {
    const t = buildTypeScale(d.typography);
    sections.push('  /* Typography */');
    sections.push(`  --font-family-body: ${t.families.body};`);
    sections.push(`  --font-family-heading: ${t.families.heading};`);
    if (t.families.mono) sections.push(`  --font-family-mono: ${t.families.mono};`);
    sections.push('');
    for (const [k, v] of Object.entries(t.sizes)) {
      sections.push(`  --font-size-${k}: ${v};`);
    }
    sections.push('');
    sections.push(`  --line-height-tight: ${t.lineHeights.tight};`);
    sections.push(`  --line-height-normal: ${t.lineHeights.normal};`);
    sections.push(`  --line-height-relaxed: ${t.lineHeights.relaxed};`);
    sections.push('');
    sections.push(`  --font-weight-body: ${t.weights.body};`);
    sections.push(`  --font-weight-body-strong: ${t.weights.bodyStrong};`);
    sections.push(`  --font-weight-heading: ${t.weights.heading};`);
    sections.push('');
  }

  if (d.spacing) {
    const s = buildSpacingScale(d.spacing);
    sections.push('  /* Spacing */');
    for (const [k, v] of Object.entries(s.tokens)) {
      sections.push(`  --space-${k}: ${v};`);
    }
    sections.push('');
  }

  if (d.radius) {
    const r = buildRadiusScale(d.radius);
    sections.push('  /* Radius */');
    for (const [k, v] of Object.entries(r.tokens)) {
      sections.push(`  --radius-${k}: ${v};`);
    }
    sections.push('');
    sections.push('  /* Component radius (resolved from base scale) */');
    sections.push(`  --radius-button: ${r.components.button};`);
    sections.push(`  --radius-card: ${r.components.card};`);
    sections.push(`  --radius-input: ${r.components.input};`);
    sections.push(`  --radius-badge: ${r.components.badge};`);
    sections.push('');
  }

  if (d.shadow) {
    const withHue: ShadowInput = {
      ...d.shadow,
      primaryHue: d.shadow.primaryHue ?? d.color?.primaryHue,
    };
    const sh = buildShadowTokens(withHue);
    sections.push('  /* Shadow */');
    for (const [k, v] of Object.entries(sh.tokens)) {
      sections.push(`  --shadow-${k}: ${v};`);
    }
    sections.push('');
  }

  if (d.color) {
    const palette = buildColorPalette(d.color);
    sections.push('  /* Color — OKLCH (source of truth) */');
    for (const [k, v] of Object.entries(palette.primary)) {
      sections.push(`  --color-primary-${k}: ${v};`);
    }
    sections.push('');
    for (const [k, v] of Object.entries(palette.neutral)) {
      sections.push(`  --color-neutral-${k}: ${v};`);
    }
    sections.push('');
    sections.push(`  --color-success-500: ${palette.semantic.success};`);
    sections.push(`  --color-warning-500: ${palette.semantic.warning};`);
    sections.push(`  --color-danger-500:  ${palette.semantic.danger};`);
    sections.push(`  --color-info-500:    ${palette.semantic.info};`);

    if (palette.dark) {
      sections.push('', '  /* Dark variants */');
      for (const [k, v] of Object.entries(palette.dark.primary)) {
        sections.push(`  --color-primary-dark-${k}: ${v};`);
      }
      sections.push('');
      for (const [k, v] of Object.entries(palette.dark.neutral)) {
        sections.push(`  --color-neutral-dark-${k}: ${v};`);
      }
    }
  }

  sections.push('}');
  sections.push('');

  return sections.join('\n');
}
